CREATE PROCEDURE SP_ValidarInicioSesion
(
	@numero CHAR(9),
	@clave	VARCHAR(100)
)
AS
BEGIN
	SELECT 
		C.IDE_CLI,
		C.NUM_CLI,
		C.CLA_CLI
	FROM LOGINS.CLIENTE AS C
	WHERE C.NUM_CLI = @numero AND C.CLA_CLI = @clave
END
GO

CREATE PROCEDURE SP_AgregarNuevaSesion
(
	@id		INT,
	@numero	char(9)
)
AS
BEGIN
	INSERT INTO LOGINS.USUARIOSACTIVOS
	VALUES(@id, @numero)
END
