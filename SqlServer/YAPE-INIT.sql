	CONSTRAINT FK_TO_CLIREC FOREIGN KEY(IDE_CLI_REC) REFERENCES LOGINS.CLIENTE(IDE_CLI),
	CONSTRAINT FK_TO_IDEYAP FOREIGN KEY(IDE_YAP) REFERENCES DATOS.YAPE(IDE_YAP),
	CONSTRAINT FK_TO_IDEDET PRIMARY KEY(IDE_DET)
)
GO

