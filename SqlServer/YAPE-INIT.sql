CREATE PROCEDURE SP_ObtenerDetallesYape
(
	@id INT
)
AS
BEGIN
	SELECT Y.IDE_YAP AS [ID],
		   Y.NRC_YAP AS [NUMERO RECIBIDOR],
		   (SELECT IC.NOM_CLI + SPACE(1) + IC.APE_CLI
			FROM LOGINS.CLIENTE IC
			JOIN DATOS.DETALLECLIYAP AS IDT ON IDT.IDE_CLI_REC = IC.IDE_CLI
			WHERE IDT.IDE_DET = DT.IDE_DET) AS [NOMBRE RECIBIDOR],
		   Y.NRZ_YAP AS [NUMERO REALIZADOR],
		   (SELECT IC.NOM_CLI + SPACE(1) + IC.APE_CLI
			FROM LOGINS.CLIENTE IC
			JOIN DATOS.DETALLECLIYAP AS IDT ON IDT.IDE_CLI_REA = IC.IDE_CLI
			WHERE IDT.IDE_DET = DT.IDE_DET) AS [NOMBRE REALIZADOR],
		   Y.MON_YAP AS [MONTO],
		   (CAST(DAY(Y.FEC_YAP) AS CHAR(2)) + SPACE(1) +
					'de' + SPACE(1) +
					(CASE MONTH(Y.FEC_YAP)
						 WHEN 1 THEN 'Enero'
						 WHEN 2 THEN 'Febrero'
						 WHEN 3 THEN 'Marzo'
						 WHEN 4 THEN 'Abril'
						 WHEN 5 THEN 'Mayo'
						 WHEN 6 THEN 'Junio'
						 WHEN 7 THEN 'Julio'
						 WHEN 8 THEN 'Agosto'
						 WHEN 9 THEN 'Septiembre'
						 WHEN 10 THEN 'Octubre'
						 WHEN 11 THEN 'Noviembre'
						 WHEN 12 THEN 'Diciembre'
					 END) + SPACE(1) +
					 'del' + SPACE(1) +
					 CAST(YEAR(Y.FEC_YAP) AS CHAR(4)) + SPACE(1) +
					 'a las' + SPACE(1) + 
					 FORMAT(Y.FEC_YAP, 'HH:mm')) AS [FECHA]
	FROM DATOS.YAPE AS Y
	JOIN DATOS.DETALLECLIYAP AS DT ON Y.IDE_YAP = DT.IDE_YAP
	JOIN LOGINS.CLIENTE AS C ON C.IDE_CLI = DT.IDE_CLI_REA
	WHERE Y.IDE_YAP = 7
END
